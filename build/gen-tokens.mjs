#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const TOKENS_DIR = join(ROOT, 'app', 'renderer', 'design')
const LIGHT_PATH = join(TOKENS_DIR, 'tokens.json')
const DARK_PATH = join(TOKENS_DIR, 'tokens.dark.json')
const OUT_CSS = join(TOKENS_DIR, 'tokens.gen.css')

const REQUIRED_VARS = [
  '--color-bg-base', '--color-bg-elevated', '--color-bg-sunken',
  '--color-text-primary', '--color-text-secondary',
  '--color-accent-primary',
  '--color-border-subtle',
  '--tab-active-bg', '--tab-inactive-bg', '--tab-hover-bg',
  '--density-tabbar-h', '--density-toolbar-h',
  '--radius-tab',
]

function flatten(obj, prefix = '') {
  const out = {}
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}-${k}` : k
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      Object.assign(out, flatten(v, key))
    } else {
      out[`--${key}`] = String(v)
    }
  }
  return out
}

if (!existsSync(LIGHT_PATH)) {
  console.warn(`[gen-tokens] no tokens.json at ${LIGHT_PATH} — skipped`)
  process.exit(0)
}

const light = JSON.parse(readFileSync(LIGHT_PATH, 'utf8'))
const dark = existsSync(DARK_PATH) ? JSON.parse(readFileSync(DARK_PATH, 'utf8')) : {}

const lightVars = flatten(light)
const darkVars = flatten(dark)

const missing = REQUIRED_VARS.filter((v) => !(v in lightVars))
if (missing.length) {
  console.error('[gen-tokens] required tokens missing in light:', missing.join(', '))
  process.exit(1)
}

let css = '/* GENERATED by build/gen-tokens.mjs — do not edit */\n\n:root {\n'
for (const [k, v] of Object.entries(lightVars)) css += `  ${k}: ${v};\n`
css += '}\n'

if (Object.keys(darkVars).length) {
  css += '\n:root[data-theme="dark"] {\n'
  for (const [k, v] of Object.entries(darkVars)) css += `  ${k}: ${v};\n`
  css += '}\n'
}

mkdirSync(dirname(OUT_CSS), { recursive: true })
writeFileSync(OUT_CSS, css)
console.log(`[gen-tokens] wrote ${OUT_CSS} (${Object.keys(lightVars).length} vars)`)
